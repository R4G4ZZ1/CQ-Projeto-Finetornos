def importar():
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()

    inseridos = 0
    duplicados = 0
    erros = []

    for cod, nome, familia, venc in instrumentos:
        try:
            c.execute(
                "INSERT INTO instrumentos (id_instrumento, nome, familia, status, data_vencimento) "
                "VALUES (?, ?, ?, 'DISPONIVEL', ?)",
                (cod, nome, familia, venc)
            )
            inseridos += 1
        except sqlite3.IntegrityError:
            duplicados += 1
            erros.append(f"  JÁ EXISTE: {cod}")

    conn.commit()
    conn.close()

    print("=" * 55)
    print("  IMPORTAÇÃO CONCLUÍDA")
    print("=" * 55)
    print(f"  ✅  Inseridos : {inseridos}")
    print(f"  ⚠   Duplicados: {duplicados}")
    if erros:
        print("\n  Códigos ignorados (já existiam no banco):")
        for e in erros:
            print(e)
    print("=" * 55)

